export default function LoadingDots({ className = '' }) {
  return (
    <div className={`flex space-x-1 items-center ${className}`}>
      {[0, 1, 2].map((i) => (
        <span
          key={i}
          className="w-2 h-2 bg-gray-400 rounded-full animate-bounce"
          style={{ animationDelay: `${i * 0.15}s` }}
        />
      ))}
    </div>
  )
}
